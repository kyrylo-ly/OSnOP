using System;
using System.IO;

internal static class Program
{
    private static readonly byte[] MessageOriginal =
    {
        0xEB, 0x18, 0x90,
        0x54, 0x65, 0x73, 0x74, 0x69, 0x6E, 0x67, 0x3A, 0x20, 0x70, 0x72, 0x69,
        0x6E, 0x74, 0x20, 0x61, 0x20, 0x6C, 0x69, 0x6E, 0x65, 0x3A,
        0x24,
        0xB4, 0x09,
        0x8D, 0x16, 0x03, 0x01,
        0xCD, 0x21,
        0xB8, 0x00, 0x4C,
        0xCD, 0x21
    };

    private static readonly byte[] MessageExtended =
    {
        0xEB, 0x28, 0x90,
        0x54, 0x65, 0x73, 0x74, 0x69, 0x6E, 0x67, 0x3A, 0x20,
        0x70, 0x72, 0x69, 0x6E, 0x74, 0x20, 0x61, 0x20, 0x6C, 0x6F, 0x6E, 0x67,
        0x65, 0x72, 0x20, 0x6C, 0x69, 0x6E, 0x65, 0x20, 0x66, 0x6F, 0x72, 0x20,
        0x6C, 0x61, 0x62, 0x33, 0x3A,
        0x24,
        0xB4, 0x09,
        0x8D, 0x16, 0x03, 0x01,
        0xCD, 0x21,
        0xB8, 0x00, 0x4C,
        0xCD, 0x21
    };

    private static int Main(string[] args)
    {
        string originalPath = args.Length > 0 ? args[0] : "lab3_message.com";
        string extendedPath = args.Length > 1 ? args[1] : "lab3_message_long.com";

        WriteCom(originalPath, MessageOriginal);
        WriteCom(extendedPath, MessageExtended);

        Console.WriteLine($"Created {Path.GetFullPath(originalPath)} ({MessageOriginal.Length} bytes)");
        Console.WriteLine($"Created {Path.GetFullPath(extendedPath)} ({MessageExtended.Length} bytes)");
        return 0;
    }

    private static void WriteCom(string path, byte[] data)
    {
        string fullPath = Path.GetFullPath(path);
        using var stream = new FileStream(fullPath, FileMode.Create, FileAccess.Write, FileShare.None);
        using var writer = new BinaryWriter(stream);
        writer.Write(data);
    }
}
